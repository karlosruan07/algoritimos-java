public class Operadores_aritmeticos {
    public static void main(String[] args){
        System.out.println("Operadores no JAVA !!");
        System.out.println("");

        int num1 = 1;
        int num2 = 1;

        int num3 = 10;
        int num4 = 3;

        System.out.println(num1 + num2);//adição
        System.out.println(num1 - num2);//subtração
        System.out.println(num1 * num2);//multiplicação

        System.out.println(num3 / num4);//divisão inteira
        System.out.println(num3 % num4);//resto da divisão
        System.out.println(++num1);//incrementa mais 1 (isso é o mesmo que sobrescrever a variável num1)
        System.out.println(--num2);//decrementa (isso é o mesmo que sobrescrever a variável num2)

    }
}

