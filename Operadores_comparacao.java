public class Operadores_comparacao {
    public static void main(String[] args){
        System.out.println("Operadores de comparação !!");

        int num1 = 10;
        int num2 = 10;
        int num3 = 20;

        System.out.println(num1 == num1);//operadores de comparação retornam apenas 2 valores possíveis (true ou false)
        System.out.println(num1 != num2);
        System.out.println(num1 < num3);
        System.out.println(num2 > num3);
        System.out.println(num1 >= num2);
        System.out.println(num1 <= num2);

    }
}
