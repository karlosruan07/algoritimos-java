public class Loop_while {
    public static void main(String[] args){
        System.out.println("LOOP WHILE NO JAVA !!\n");

        int num1 = 0;

        while(num1 <= 10){
            System.out.println(num1);
            num1++;
        }

        System.out.println("=-=-=-=-=-=-=-=-=-=-=-=");

        int num2 = 10;

        do{
            System.out.println(num2);
            num2--;
        }while(num2 >= 0);


    }
}
