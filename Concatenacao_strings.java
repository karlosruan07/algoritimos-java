public class Concatenacao_strings {
    public static void main(String[] args){
        System.out.println("CONCATENAÇÃO DE STRING !! \n");

        String nome = "Ruan";
        String sobrenome = "Carlos";

        System.out.println(nome + " " + sobrenome + "\n");
        System.out.println(nome.concat(sobrenome));

        String numero_string = "20";
        int num = 10;

        System.out.println(numero_string + num);//número e string são concatenadas

        

    }
}
