public class Loop_for_reach {
    public static void main(String[] args){
        System.out.println("FOREACH NO JAVA !!\n");

        String[] nome_array = {"Ruan", "carlos", "tavares"};

        for(String i: nome_array){//nome da variável que será criada e armazenará cada valor do array
            //nome : é o nome do array
            System.out.println(i);

        }
    }
}
