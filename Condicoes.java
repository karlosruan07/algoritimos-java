public class Condicoes {
    public static void main(String[] args){
        System.out.println("CONDIÇÕES NO JAVA !!\n");

        int num1 = 10;
        int num2 = 20;
        if(num1 < num2){//java diferencia maiuscula de minusculas if != IF; 
            System.out.println("10 é menor");
        }else if (num2 > num1){
            System.out.println("10 é menor");
        }
        else{
            System.out.println("10 é igual !");
        }
    }
}
